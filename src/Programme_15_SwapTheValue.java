public class Programme_15_SwapTheValue {
    public static void main(String[] args) {
        int x, y, z;
        x = 10;
        y = 20;
        System.out.println("Before swapping: x,y,= "+x+",+ +y ");
        z = x;
        x = y;
        y = z;
        System.out.println("After swapping: x,y = "+x+",+ +y ");
    }

}
